package com.Task62.Features;

public class LargestNumber {
	public static void main(String[] args) {
		int a = 25;
        int b = 18;

        if (a > b) {
            System.out.println(a + " is greater.");
        } else if (b > a) {
            System.out.println(b + " is greater.");
        } else {
            System.out.println("Both numbers are equal.");
        }
	}
}
